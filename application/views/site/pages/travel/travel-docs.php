        <h2>Travel Documents</h2>

        <p>

        </p><p>DO  NOT LEAVE THIS TASK TO THE LAST MINUTE AS IT CAN SOMETIMES TAKE UPWARDS OF A  MONTH TO HAVE YOUR VISA OR PASSPORT RENEWAL approved and returned to you.&nbsp; If you have qualified for the trip and are in  need of any of the following travel documents, start the application process  IMMEDIATELY. In order to complete your Convention registration, you will need  information from your passport and visa(s).</p>

        <p><strong>Passports</strong> <br>

            Everyone  must have a passport valid for at least 6 months after June 2, 2016 your  departure date from Barcelona, Spain. Most destinations, including Spain,  require that you have adequate unused pages in your passport, allowing for any  necessary stamps upon arrival and departure. We recommend that you have at  least two free pages in the Visas section of your passport before any  international travel.</p>

        <p><strong>Schengen Visa</strong><br>

            Nationals  of the following countries, travelling on a passport from said countries are  required to apply for a Schengen for entry into the EU and Spain &ndash; <strong>Cayman Islands**,</strong> <strong>Guyana, and Jamaica</strong></p>

        <p><strong>NOTE: </strong>The short-stay  Visa Waiver Agreement signed on 28 May 2015 in Brussels, between the European  Union and Trinidad and Tobago, Saint Lucia, Granada, St. Vincent and the  Grenadines and Dominica allows the citizens of these countries to visit the  Schengen area for a short stay of up to 90 days without requiring a visa, as  long as the purpose of the visit is not to carry out a paid activity.&nbsp; </p>

        <p>**Citizens  of Cayman Islands DO NOT require a visa to Spain if their passports contain any  of the following endorsements:</p>

        <p>1.  British Citizen;<br>

            2.  British Nationals (Overseas);<br>

            3.  British Overseas Territories Citizens who have the right of abode in the United  Kingdom;<br>

            4.  British Subjects who have the right of abode in the United Kingdom.</p>

        <p><strong>HOW TO APPLY (CITIZENS AND RESIDENTS  OF JAMAICA): </strong><br>

            The  Embassy of Spain in Jamaica issues Schengen visas to Jamaican citizens and  other nationals with legal residence in Jamaica and will be required to  schedule an appointment for the submission of visa applications and biometrics.</p>

        <p>To set a visa appointment, please complete the  below appointment application form and forward the document via e-mail to  emb.kingston@maec.es. The subject of this e-mail must read “Visa Appointment”.<br>

            <br>

            If for any reason you miss the scheduled  appointment then you must follow the procedure above and submit a request for a  new date and time via e-mail.<br>

            <br>

            The Consular Section is open to the public from  9:00 am to 1:00 pm Monday to Friday.</p>

        <p>Appointment  Request Form: <a href="http://www.exteriores.gob.es/Embajadas/KINGSTON/en/Embajada/Documents/Appointment%20Request%20Form.doc">http://www.exteriores.gob.es/Embajadas/KINGSTON/en/Embajada/Documents/Appointment%20Request%20Form.doc</a></p>

        <p>Schengen Visa Application Form: <br>

            <a href="http://www.exteriores.gob.es/Embajadas/KINGSTON/en/Embajada/Documents/Schengen%20Visa%20Application%20Form%20(ENG).doc">http://www.exteriores.gob.es/Embajadas/KINGSTON/en/Embajada/Documents/Schengen%20Visa%20Application%20Form%20(ENG).doc</a></p>

        <p>Schengen Visa Requirements:<br>

        </p>

        <ol>

            <li> Schengen  visa application form duly completed, dated and signed by the applicant</li>

            <li>One  passport size photograph</li>

            <li>Valid  Passport (Passport must be valid for at least 3 months after validity of the  visa).&nbsp; Where the passport has been  issued less than one year prior to Schengen Visa application submission,  applicant must also present the previous passport. For citizens of countries  other than Jamaica, Dominica, Antigua and Barbuda, St. Kitts and Nevis and The  Bahamas, the corresponding resident and/or work permit is required.</li>

            <li>Photocopy  of passport data page&nbsp;&nbsp;&nbsp;&nbsp; </li>

            <li> Recent  letter from the bank stating account balance for the last three months  (individual month balances), date of the last deposit, and the currency.</li>

            <li>Travel  itinerary indicating name of passenger (it is not necessary to purchase the  airline ticket prior to the submission of the visa application)</li>

            <li> Original  job letter indicating date of employment, duration of contract, post in  company, salary and number of vacation days. If self-employed, original and  copy of the certificate of registration of the business.</li>

            <li>Hotel  reservation including address of hotel, number of days reserved in the name of  applicant.&nbsp; If staying at someone’s home  in the Schengen destination state, an original letter of invitation processed  by the National Police or relevant Authorities must be submitted.</li>

            <li>If  travelling on the invitation of a company, letter of invitation issued by the  company in the country of destination stating the purpose of travel, the length  of stay, place of abode and specifying who is covering the cost of the trip.</li>

            <li>Original  and photocopy of the Travel Health Insurance in the name of the applicant, with  a minimum coverage of thirty thousand (30,000) Euros for emergency medical treatment.  There should be additional coverage for repatriation of mortal remains. The  insurance company should have at least an office in a Schengen State and should  cover all costs (the insured should not have to make any up-front payments).</li>

        </ol>

        <p><strong>IMPORTANT!</strong><br>

        </p>

        <ul>

            <li>The visa  processing time is 15 days. This could be prolonged if additional information  or an interview is required.&nbsp; Please bear  this in mind when applying for the visa so as to submit said application with  sufficient time for its processing.</li>

            <li>Please include  originals of all documents required. The Embassy will evaluate the  documentation presented and will request additional where necessary.</li>

        </ul>

        <p><strong>Other Visas</strong><br>

            The majority of connections will be routed through  the US, therefore all participants must hold valid  documentation for entry into the US or obtain a US Visa.</p>

        <p>For information and Visa application forms for  travel through the US, please click here <br>

            <a href="http://travel.state.gov/visa/temp/types/types_1264.html">http://travel.state.gov/visa/temp/types/types_1264.html</a> or visit <a href="https://evisaforms.state.gov/ds156.asp">https://evisaforms.state.gov/ds156.asp</a> to directly access the US Visitor Visa application  form.</p>

        <p><strong>Proof of travel</strong><br>

            Sagicor  will provide a letter confirming the participation of you and/or your  partner/travel companion (see the Club Rules) on the programme as well as  Sagicor's financial responsibility for your accommodation and the majority of  your meals, transportation, activities etc. This letter must be presented to  the appropriate Embassy or Consulate along with the completed application.</p>

