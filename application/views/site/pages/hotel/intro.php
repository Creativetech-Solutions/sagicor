        <h2>Hotel Arts</h2>

        <p>Designed by distinguished architect Bruce  Graham of Skidmore, Owings and Merrill, the Hotel Arts is part of Barcelona’s  recent cultural renaissance and as modern as the city itself. Built to coincide  with the 1992 Olympic Games, this luxury hotel is a landmark on the Barceloneta  boardwalk: 44 stories of blue glass and steel soaring high above the Oceanfront.  Inside, a  spectacular collection of Spanish contemporary art give way to an environment of elegance and refinement, filled  with luxurious places to explore and discover at your leisure.</p>

        <p>The terraced gardens bask in the dappled  shade of Frank Gehry’s monumental golden fish sculpture, one of Barcelona’s  most iconic works of contemporary art. Relax in the outdoor tub, lose yourself  in a good book, or simply stretch out on a sun lounger and savour the sense of  natural seclusion. Next to the gardens, the pool is the perfect spot for a  refreshing dip, perhaps followed by a pool-side cocktail.</p>

        <p>The Hotel Arts overlooks the majestic Mediterranean  and Port Olimpic and marina. Barcelona’s famous beach and boardwalk is surrounded  by lush terraces, lively promenades and charming cafes. The district of  Barceloneta is indeed a hive of characterful bars, cafes and local Tapas  restaurants. The hotel is also just a short stroll away from the city’s  historical centre, and just 20 minutes from Barcelona’s Airport.</p>

