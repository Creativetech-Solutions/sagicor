                <h3>The hallmark grid pattern of the city came from a revolutionary utopian master plan</h3>

                Most of the city of Barcelona was designed by Ildefons Cerda i Sunyer for the 19th-century extension of the city.  The plan, called Eixample, was intended to create the perfect environment for Barcelona's fast expanding population.  He considered traffic and transport along with sunlight and ventilation for the design of his characteristic octagonal blocks, where the streets broaden at every intersection making for greater visibility, better ventilation, and more significant open spaces.  In the end, however, the plan wasn't built following all the utopian characteristics he envisioned in his plan.</li>

