                <h3>The real story behind the Columbus monument</h3>

                The Columbus Monument was built in 1888 for the World Exposition. After reconstruction works, the monument was reopened in June 2013. It unites two of the most important roads of Barcelona: the traditional Ramblas and Passeig de Colon. There are many interpretations about the location Columbus is pointing. A commonly mistaken idea is that he is pointing towards America, but America is in the opposite direction. Some believe the creator decided to create the statue pointing his hand out to sea, illustrating his achievements. Other opinions say that he points towards India, the destination he was originally trying to reach via a new route when he bumped into America. Finally, another school of thought believes that the statue points out the route to Genoa, which many say is his hometown.</li>

