                <h3>La Rambla is not just one street... it's five streets</h3>

                La Rambla is probably the most famous street in Barcelona, but in reality it consists of 5 boulevards -or ramblas- joined to make one long promenade. It starts from Placa Catalunya and end at the Columbus statue by the waterfront.  For this reason, this 2-kilometer long boulevard is also commonly known in plural as Las Ramblas. Thousands of people stroll on Las Ramblas every day and night, where they watch the performing street artists, shop, or sit for a coffee or glass of Cava. Las Ramblas is the major centre of activities in the city and is undoubtedly the most famous promenade of Barcelona, and probably in all of Spain.</li>

