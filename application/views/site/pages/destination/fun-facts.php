                <h3>Gaudi was not the original architect of Sagrada Familia</h3>

                Even though La Sagrada Familia is one of the major works of Antoni Gaudi, he was not the first architect to be appointed to work on it.  Architect Francisco de Paula del Villar was the first to be commissioned to design the church. Construction of the crypt of the church began on March 19, 1882, following Villar's design.  It wasn't until 1883 when Antoni Gaudi started working on the project.  On March 18, 1883, Villar retired from the project, and Gaudi assumed responsibility for its design, which he changed dramatically.</li>

